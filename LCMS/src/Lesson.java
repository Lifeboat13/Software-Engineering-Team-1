
public class Lesson {
    private String lessonID;    
    private String lessonName;
    private String goal1ID;
    private String goal2ID;
    private String goal3ID;

    public String getLessonID() {
        return lessonID;
    }

    public void setLessonID(String lessonID) {
        this.lessonID = lessonID;
    }

    public String getLessonName() {
        return lessonName;
    }

    public void setLessonName(String lessonName) {
        this.lessonName = lessonName;
    }

    public String getGoal1ID() {
        return goal1ID;
    }

    public void setGoal1ID(String goal1ID) {
        this.goal1ID = goal1ID;
    }

    public String getGoal2ID() {
        return goal2ID;
    }

    public void setGoal2ID(String goal2ID) {
        this.goal2ID = goal2ID;
    }

    public String getGoal3ID() {
        return goal3ID;
    }

    public void setGoal3ID(String goal3ID) {
        this.goal3ID = goal3ID;
    }
    
    
        
        
}
